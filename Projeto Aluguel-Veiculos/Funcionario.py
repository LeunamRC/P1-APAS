

class Funcionario:

    def get_id(self):
        return self._id

    def set_id(self, _id):
        self._id = _id

    def get_nome(self):
        return self.nome

    def set_nome(self, nome):
        self.nome = nome

    def get_turno(self):
        return self.turno

    def set_turno(self, turno):
        self.turno = turno