

class Carro:

    def get_id(self):
        return self._id

    def set_id(self, _id):
        self._id = _id

    def get_marca(self):
        return self.marca

    def set_marca(self, marca):
        self.marca = marca

    def get_modelo(self):
        return self.modelo

    def set_modelo(self, modelo):
        self.modelo = modelo

    def get_placa(self):
        return self.placa

    def set_placa(self, placa):
        self.placa = placa

    def get_preco(self):
        return self.preco

    def set_preco(self, preco):
        self.preco = preco