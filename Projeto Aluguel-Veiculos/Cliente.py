class Cliente:

    def get_id(self):
        return self._id

    def set_id(self, _id):
        self._id = _id

    def get_nome(self):
        return self.nome

    def set_nome(self, nome):
        self.nome = nome

    def get_telefone(self):
        return self.telefone

    def set_telefone(self, telefone):
        self.telefone = telefone

    def get_email(self):
        return self.email

    def set_email(self, email):
        self.email = email

    def get_cpf(self):
        return self.cpf

    def set_cpf(self, cpf):
        self.cpf = cpf