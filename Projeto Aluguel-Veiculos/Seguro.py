

class Seguro:

    def get_id(self):
        return self._id

    def set_id(self, _id):
        self._id = _id

    def get_preco(self):
        return self.preco

    def set_preco(self, preco):
        self.preco = preco

    def get_tipo(self):
        return self.tipo

    def set_tipo(self, tipo):
        self.tipo = tipo